// ファイル責務: デッキを構成するカード1枚1枚を識別する型定義をまとめ、
// 他のモジュールが共通の構造を参照できるようにする。
// ロジックは持たず、型エイリアスのみを提供することでデッキパースや状態管理での不整合を防ぐ。

/**
 * デッキ内の1枚ごとの情報を表す型。
 * 例: { cardId: "PL!SP-bp1-004-R", nth: 3 } はカード番号 "PL!SP-bp1-004-R" の3枚目の実体を表す。
 * cardId はカード番号文字列、nth は同一カード内での通し番号を number で保持する。
 */
export type DeckCardInstance = {
  // cardId: カード番号を表す文字列。パース済みの JSON からそのまま受け取り、表示や状態管理で識別に使う。
  cardId: string;
  // nth: 同じ cardId 内での n 枚目を表す番号。1始まりで連番化し、重複カードを区別するために使用する。
  nth: number;
};
